const fs = require("fs");
const path = require("path");
const { execFileSync } = require("child_process");

const SOURCE_REPO = "https://github.com/j2kenton/revamp.git";
const rootDir = path.resolve(__dirname, "..");
const templateDir = path.join(rootDir, "template");

const excludedNames = new Set([
  ".git",
  ".github",
  ".vscode",
  "node_modules",
  ".next",
  ".turbo",
  ".vercel",
  ".cache",
  "coverage",
  "dist",
  "build",
  "dev-resources",
  "package-lock.json",
  "pnpm-lock.yaml",
  "yarn.lock",
  "AI_AGENT_WORKFLOW.md",
  "SWR_CHECKLIST.md",
  "SWR_SUMMARY.md",
]);

function shouldExclude(entryName) {
  return excludedNames.has(entryName) || entryName === ".DS_Store" || entryName.endsWith(".md");
}

function copyFiltered(source, target) {
  const stat = fs.lstatSync(source);

  if (stat.isDirectory()) {
    fs.mkdirSync(target, { recursive: true });
    for (const entry of fs.readdirSync(source)) {
      if (shouldExclude(entry)) continue;
      copyFiltered(path.join(source, entry), path.join(target, entry));
    }
    return;
  }

  if (stat.isSymbolicLink()) {
    const linkTarget = fs.readlinkSync(source);
    fs.symlinkSync(linkTarget, target);
    return;
  }

  fs.copyFileSync(source, target);
}

function main() {
  const tempDir = fs.mkdtempSync(path.join(rootDir, ".template-tmp-"));
  const checkoutDir = path.join(tempDir, "source");

  try {
    execFileSync("git", ["clone", "--depth", "1", SOURCE_REPO, checkoutDir], {
      stdio: "inherit",
    });

    fs.rmSync(templateDir, { recursive: true, force: true });
    fs.mkdirSync(templateDir, { recursive: true });

    for (const entry of fs.readdirSync(checkoutDir)) {
      if (shouldExclude(entry)) continue;
      copyFiltered(path.join(checkoutDir, entry), path.join(templateDir, entry));
    }

    fs.writeFileSync(
      path.join(templateDir, "README.md"),
      [
        "# ReVamp",
        "",
        "A Next.js starter project generated by ReVamp VS Code.",
        "",
        "## Getting Started",
        "",
        "1. Install dependencies with your preferred package manager.",
        "2. Start the development server from the generated project folder.",
        "",
      ].join("\n")
    );

    const templatePackagePath = path.join(templateDir, "package.json");
    const templatePackage = JSON.parse(fs.readFileSync(templatePackagePath, "utf8"));
    delete templatePackage.dependencies?.init;
    fs.writeFileSync(templatePackagePath, `${JSON.stringify(templatePackage, null, 2)}\n`);
  } finally {
    fs.rmSync(tempDir, { recursive: true, force: true });
  }
}

main();
